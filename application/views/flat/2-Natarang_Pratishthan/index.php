			<h1>Natarang Pratishthan</h1>
			<p>The Natarang Pratishthan was set up in 1989 to fill the void in the knowledge and research of theatre activities, particularly in North India where theatre movement, though multifaceted and innovative, has not been collectively recorded at any one centre for posterity. Established by eminent author, poet and critic Nemichandra Jain as a charitable trust, the Pratishthan has since grown into a valuable resource centre for Indian and Asian theatre.</p>
			<p>Initially started as part of a large personal collection of newspaper clippings, brochures, books, journals, scripts, photographs, invitation cards, cassettes and seminar papers, it has since grown to include rare collections that have been gifted, or bought, by the Pratishthan for purposes of archival documentation. More recently, the Pratishthan has also supported theatre activities directly, by promoting weekend theatre projects, or theatre festivals. Today, it is one of Indiaâ€™s most comprehensive research centres for the study of theatre over the last century.
			</p>
			<p>
			Its outreach activities have made space for discussions and seminars on performances, critics, actors, directors, subject material and more, so it can contribute back to theatre not only in terms of past material, but also as a catalyst for currently needed intelectual debate.</p> 
			<h2>Objectives of Natarang Pratishthan</h2>
			<ul>
				<li>The maintaining and expanding of an archive containing printed and audio visual material on theatre and literature, especially Indian and Asian theatre.</li>
				<li>The promotion of theatre, literature, arts and culture, and providing facilities for research, study, surveys and documentation.</li>
				<li>The organisation of dialogue and interaction between artistes through cultural and creative projects.</li>
				<li>The collating of rare audio-visual material for research through various creative forums.</li>
				<li>The establishment of fellowships and scholarships.</li>
				<li>The publication of journals, monographs, books and catalogues of reference materials.</li>
				<li>The promotion and establishment of performing units.</li>
				<li>Building links with Asian countries to re-establish and reinvigorate links in the performance sector.</li>
			</ul>
			<h2>Looking Ahead</h2>
			<p>Natarang Pratishthan intends to develop its facilities and resources through:</p>
			<ul>
				<li>technological upgrading and computerisation of its archives</li>
				<li>documentation of major productions, directors, playwrights, etc. on varied media</li>
				<li>addition of listening rooms, viewing rooms, studios, audio-visual documentation library etc.</li>
				<li>undertaking publication of monographs, research documents and bibliographies</li>
				<li>instituting fellowships, scholarships and awards to deserving persons for study, research and promotion of performing arts, literature and culture</li>
				<li>developing a modest arts complex</li>
				<li>maintaining a corpus for smooth functioning and the execution of its future plans for which it would approach all such institutions and individuals who value for it.</li>
			</ul>                       
